# bot/states/game.py

"""
FSM-состояния игрового модуля.

Отвечает за:
- хранение состояния активного игрового диалога;
- хранение состояния анализа завершённого диалога.

Как работает:
- состояние in_dialog означает, что пользователь находится внутри игрового диалога;
- состояние analyzing_dialog означает, что диалог уже завершён и идёт аналитика.

Что принимает:
- ничего.

Что возвращает:
- класс состояний GameStates.
"""

from aiogram.fsm.state import State, StatesGroup


class GameStates(StatesGroup):
    """
    Состояния игрового модуля.

    Отвечает за:
    - обозначение активного игрового диалога;
    - обозначение этапа аналитики завершённого диалога.

    Как работает:
    - используется обработчиками игрового сценария и таймером.

    Что принимает:
    - ничего.

    Что возвращает:
    - набор состояний FSM.
    """

    in_dialog = State()
    analyzing_dialog = State()
