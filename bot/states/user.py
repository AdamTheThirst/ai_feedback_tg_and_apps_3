# bot/states/user.py

"""
FSM-состояния пользовательского сценария регистрации.

Отвечает за:
- ожидание имени пользователя при первом запуске бота.

Как работает:
- если пользователь впервые запускает бота, его просят ввести имя;
- до получения имени пользователь находится в специальном состоянии.

Что принимает:
- ничего.

Что возвращает:
- класс состояний UserStates.
"""

from aiogram.fsm.state import State, StatesGroup


class UserStates(StatesGroup):
    """
    Состояния пользовательской регистрации.

    Отвечает за:
    - ожидание имени пользователя.

    Как работает:
    - waiting_name используется сразу после первого запуска,
      если у пользователя ещё нет имени в таблице users.

    Что принимает:
    - ничего.

    Что возвращает:
    - набор состояний FSM.
    """

    waiting_name = State()
